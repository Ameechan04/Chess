public class Player {


    String name;
    boolean colour;

    public Player(String name, boolean colour) {
        this.name = name;
        this.colour = colour;

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isColour() {
        return colour;
    }

    public void setColour(boolean colour) {
        this.colour = colour;
    }



}
